import numpy as np

arr = np.array([12, 21, 22])

print("Original Array :", arr)

print("Addition :", arr + 5)
print("Subtraction :", arr - 5)
print("Division :", arr / 5)
print("Multiplication :", arr * 5)
print("Power :", arr ** 5)